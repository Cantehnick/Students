package by.htp.Arrays.model;

public class Student {

	private String name;
	private int year;
	private int age;

	public Student() {

	}

	public Student(String name, int age, int year) {
		this.name = name;
		this.age = age;
		this.year = year;
	}

	public String getName() {
		return this.name;

	}

	public int getYear() {
		return this.year;
	}

	public int getAge() {
		return this.age = age;
	}

}
